const rootDir = path.dirname(fileURLToPath(import.meta.url));
const targetPath = path.resolve(rootDir, '../src/data/generalRequirements/generatedCourseLists.ts');

function extractVisibleText(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/gi, ' ')
    .replace(/&#8211;|&ndash;/gi, '-')
    .replace(/&amp;/gi, '&')
    .replace(/\s+/g, ' ')
    .trim();
}

function pickRelevantText(html, markers) {
  const lower = html.toLowerCase();
  const start = markers
    .map((marker) => lower.indexOf(marker.toLowerCase()))
    .find((index) => index >= 0);

  if (start === undefined) {
    throw new Error(`Could not find any marker: ${markers.join(', ')}`);
  }

  return extractVisibleText(html.slice(start));
}

function extractCourseIds(text) {
  return new Set(
    [...text.matchAll(/\b\d{6,8}\b/g)]
      .map((match) => match[0].padStart(8, '0'))
  );
}

function renderSet(name, ids) {
  const sorted = [...ids].sort();
  const body = sorted.map((id) => `  '${id}',`).join('\n');
  return `export const ${name} = new Set<string>([\n${body}\n]);`;
}

function renderCourseList(name, courses) {
  const body = courses
    .map((course) => `  { id: '${course.id}', name: ${JSON.stringify(course.name)}, credits: ${course.credits} },`)
    .join('\n');
  return `export const ${name} = [\n${body}\n] as const;`;
}

async function fetchHtml(url) {
  const response = await fetch(url, {
    headers: {
      'user-agent': 'technion-ee-planner-sync/1.0',
    },
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.text();
}

async function hasGeneratedFallback() {
  try {
    await access(targetPath);
    const contents = await readFile(targetPath, 'utf8');
    return contents.includes('export const englishCourseIds') && contents.includes('export const melagCourseIds');
  } catch {
    return false;
  }
}

async function buildOutput() {
  const [englishHtml, melagHtml] = await Promise.all([
    fetchHtml(ENGLISH_URL),
    fetchHtml(MELAG_URL),
  ]);

  const englishText = pickRelevantText(englishHtml, [
    'להלן רשימת המקצועות הנלמדים בשפה האנגלית לפי סמסטרים',
    'מקצועות באנגלית',
  ]);
  const melagText = pickRelevantText(melagHtml, [
    'רשימת מקצועות העשרה',
    'לימודי העשרה',
  ]);

  const englishIds = extractCourseIds(englishText);
  const melagIds = new Set(
    [...extractCourseIds(melagText)].filter((id) => !id.startsWith('039'))
  );

  for (const id of SOMETIMES_ENGLISH_MELAG_IDS) {
    englishIds.delete(id);
  }

  if (englishIds.size === 0 || melagIds.size === 0) {
    throw new Error(`Parsed empty course list: english=${englishIds.size}, melag=${melagIds.size}`);
  }

  return `// Generated by scripts/sync-general-requirements.mjs
// Do not edit by hand.

${renderSet('englishCourseIds', englishIds)}

${renderSet('melagCourseIds', melagIds)}

${renderSet('sometimesEnglishMelagCourseIds', SOMETIMES_ENGLISH_MELAG_IDS)}

${renderCourseList('humanitiesFreeElectiveCourses', HUMANITIES_FREE_ELECTIVE_COURSES)}
`;
}

async function main() {
  try {
    const output = await buildOutput();
    await writeFile(targetPath, output, 'utf8');
  } catch (error) {
    if (await hasGeneratedFallback()) {
      console.warn('General requirements sync failed; using committed generated course lists.');
      console.warn(error);
      return;
    }

    throw error;
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
